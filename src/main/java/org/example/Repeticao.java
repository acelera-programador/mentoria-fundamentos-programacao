package org.example;

public class Repeticao {

    public static void main(String[] args) {

        //for(inicializacao; condicao; incremento)
        for(int x = 1; x <= 5; x++){
            System.out.println("Exemplo com for: " + x);
        }



        //while(condicao){

        int a = 1;
        while(a <= 5){
            System.out.println("Exemplo com while: " + a);
            a++;
        }





    }

}
